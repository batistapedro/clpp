<h2>Definición</h2>
<p id="pacercade">
La base legal referida a la creación, integrantes, actuación, y definición del Consejo Local de Planificación Pública, se enmarca en:<br><br>
* La Constitución de la República Bolivariana de Venezuela, Titulo IV del Poder Público, Capítulo IV del Poder Público Municipal, Artículo 182.<br><br>
* El Decreto No. 1.528 con Fuerza de Ley Orgánica de Planificación, Título III, Instancias de Coordinación y Formulación en la Planificación, Capítulo II, Instancias Regionales, Estadales y Municipales, Artículo 26.<br><br>
* La Ley de los Consejos Locales de Planificación Pública Capitulo I, Principios Fundamentales, Artículo 2.<br><br>
A partir de la revisión documental del marco legal, se concibe el Consejo Local de Planificación Pública 8CLPP9, como la Instancia de participación ciudadana, encargada del proceso de formulación , seguimiento, control y evaluación de los planes de desarrollo municipales, garantizados así, resultados dirigidos al desarrollo del espacio geopolítico en función de la prosperidad económica y el bienestar social de su población. El escenario para la práctica de la planificación participativa y el ejercicio permanente de la democracia directa y protagónica.<br><br>
