<h2>Derechos, Deberes Y Atribuciones De Los Concejales</h2>
<p id="pacercade">
<strong>Deberes:</strong><br><br>
* Coordinar el proceso de toma de decisiones.<br><br>
* Velar por el cumplimiento de las funciones establecida en la Ley, Decretos y Ordenanzas Municipales.<br><br>
* Participar activamente mediante el aporte de experiencias, criterios y propuestas para satisfacer las necesidades de la comunidad en pro del desarrollo municipal.<br><br>
* Mantener vinculación permanente con sus representados.<br><br>
* Asesorar y atender las solicitudes de la comunidad e informar sistemáticamente sobre sus gestiones.<br><br>
* Rendir Cuenta anual de su gestión al sector que representa.<br><br>
<strong>Derechos:</strong><br><br>
* Solicitar y recibir información oportuna y confiable, en materia de competencia del Consejo.<br><br>
* Proponer, acoger y rechazar propuestas presentadas al Consejo.<br><br>
* Recibir asistencia técnica, que facilite el cumplimiento de los deberes y tareas que le correspondan.<br><br>
<strong>ATRIBUCIONES:</strong><br><br>
<strong>Presidente:</strong><br><br>
* Velar por el cumplimiento de la misión y funciones encomendadas al CLPP.<br><br>
* Garantizar la vinculación de los plantes municipales con el estadal y nacional.<br><br>
* Garantizar y estimular la participación social conforme a los establecido en la Constitución de la República Bolivariana de Venezuela, la Ley Orgánica de Planificación y la Ley del Consejo Local de Planificación Pública.<br><br>
* Rendir cuenta pública sobre la gestión anual de CLPP.<br><br>
* Presidir, abril, prorrogar, suspender, levantar las sesiones y dirigir el debate del CLPP.<br><br>
* Dirigir el CLPP.<br><br>
* Garantizar el buen funcionamiento de los servicios de apoyo a la gestión del CLPP.<br><br>
* Ejercer la representación del CLPP.<br><br>
* Convocar y presidir las reuniones de la Comisión Coordinadora.<br><br>
* Disponer lo relativo a la salvaguarda y protección del patrimonio del Consejo.<br><br>
* Firmar acuerdos, resoluciones y demás comunicaciones que sean emitidos en nombre del Consejo.<br><br>.
* Garantizar la información sobre el registro y control de las asociaciones de las comunidades organizadas participantes ante el CLPP.<br><br>
* Solicitar a los órganos del sector público y privado, cooperación para el cumplimiento de las funciones del Consejo.<br><br>
* Las demás atribuciones que les señala la Ley.<br><br>
<strong>Sala Técnica:</strong><br><br>
* La Ley del CLPP, en el Capitulo V, Artículo 19, referido a la Sala Técnica, le asigna las siguientes atribuciones:<br><br>
* Proveer la Información, integral automatizada, en la medida de lo posible con el propósito de asegurar la información sectorial codificada, necesaria para la planificación, el control de gestión y la participación de la comunidad organizada.<br><br>
* Crear y poner en funcionamiento la Unidad de Plantes y Proyectos, integrada por profesionales especializados en materia de planificación.<br><br>
* Garantizar la información sobre el registro y control de las asociaciones de las comunidades organizadas participantes ante el Consejo Local de Planificación Pública.<br><br>
Como otras funciones a considerar se proponen:<br><br>
* Evaluar la factibilidad de los planes, programas y proyectos, bajo criterios metodológicos y la normativa contemplada en la Ley Orgánica de Planificación presentados ante el CLPP.<br><br>
* Emitir criterios sobre aspectos técnicos del desarrollo municipal.<br><br>
* Elaborar metodología par ala formulación de planes, programas y proyectos.<br><br>
* Elaborar propuestas de planes, programas y proyectos.<br><br>
* Dar asistencia técnica en materia de planificación.<br><br>
* Proveer información integrar automatizada.<br><br>
* Las demás que le sean asignadas por el CLPP.<br><br>
<strong>Comisión Coordinadora:</strong><br><br>
* Coordinar la información para los procesos de análisis y toma de decisiones del CLPP.<br><br>
* Proponer las actividades del CLPP y establecer el orden del día.<br><br>
* Revisar el Plan operativo anual.<br><br>
* Garantizar el normal funcionamiento de las Comisiones.<br><br>
* Supervisar el cumplimiento de las disposiciones legales y reglamentarlas.<br><br>
* Las demás que le sean asignadas por el CLPP.<br><br>
<strong>Comisión Permanente:</strong><br><br>
* Organizar y promover la participación ciudadana.<br><br>
* Estudiar las propuestas de la comunidad y las materias conexas a ser discutidas en las sesiones del CLPP.<br><br>
* Coordinar las acciones del CLPP y la interacción con la comunidad, en el área de su competencia.<br><br>
<strong>Secretaría:</strong><br><br>
Ejercer las funciones que le competen como Secretaria Ejecutiva durante las sesiones del CLPP y la Comisión Coordinadora:<br><br>
* Verificar el quórum al comienzo de cada sesión o solicitud de la Presidencia.<br><br>
* Leer todos los documentos que le sean requeridos durante las sesiones, por el Presidente.<br><br>
* Levantar las actas de las sesiones del CLPP.<br><br>
* Atender a los ciudadanos que soliciten información general en función de sus necesidades de participación.<br><br>
* Elaborar la agenda de los puntos a ser tratados en las sesiones del CLPP.<br><br>
* Llevar control de asistencia de las sesiones del CLPP.<br><br>
* Proveer a los consejeros de los documentos de identificación que los acreditan como tales.<br><br>
* Llevar al día las actas de sesiones del CLPP, expedientes, documentos y demás registros necesarios.<br><br>
* Expedir certificaciones de las actas, documentos en proceso o de archivo, previa aprobación del Presidente.<br><br>
* Enviar las comunicaciones que le correspondan en el ejercicio de sus funciones.<br><br>
* Conservar y custodiar el archivo del CLPP.<br><br>
* Elaborar expedientes de los proyectos presentados.<br><br>
* Ejercer guardia y custodia de los sellos del CLPP.<br><br>
* Distribuir oportunamente a los consejeros la agencia de trabajo, la cuenta y el orden del día.<br><br>
* Asistir logísticamente el desarrollo de las sesiones.<br><br>
* Coordinar el ceremonial y los servicios de protocolo.<br><br>
* Rendir cuenta a la Presidencia.<br><br>
* Llevar registro de las organizaciones civiles del Municipio.<br><br>
* Las demás que le sean asignadas por el CLPP y el Presidente.<br><br>
