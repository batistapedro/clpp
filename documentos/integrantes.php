<h2>Integrantes</h2>
<p id="pacercade">
De acuerdo al Artículo 182 de la Constitución, Capítulo IV, del Poder Municipal y el Artículo 3 de la Ley del Consejo Local de Planificación Pública (CLPP) Capítulo I, Principios Fundamentales referido a la Integración, el Consejo para el ejercicio de sus funciones, estará conformado por:<br><br>
* El presidente o presidenta, quien será el Alcalde o Alcaldesa.<br><br>
* Concejales y Concejalas.<br><br>
* Presidentes o presidentas de las Juntas Parroquiales.<br><br>
* El o los representantes por sectores de las organizaciones de la sociedad y organizadas.<br><br>
* El o los representantes de las comunidades o pueblos indígenas, donde los hubiere.<br><br>
* El número de representantes de la sociedad civil, incluyendo el o los del pueblo indígena donde los hubiere, es igual a la sumatoria más uno de los siguientes integrantes el Alcalde, los Concejales y los representantes de las Juntas Parroquiales.<br><br>
* El periodo de duración para el ejercicio de las funciones, por integrantes, lo establece la Ley del CLPP, en el Capítulo I, de los Principios Fundamentales, Artículo 7.<br><br>
