<h2>Institucionalización</h2>
<p id="pacercade">
El funcionamiento del Consejo, es responsabilidad del Alcalde o Alcaldesa o el funcionario accidental, en los primeros veinte (20) días de la vigencia de esta Ley, conforme el Capitulo VII, Artículo 25, recomendándose la realización de actividades concernientes al proceso de institucionalización del mismo, entre ellas:<br><br>
* Elaborar la Ordenanza que regula la organización y funcionamiento del CLPP.<br><br>
* Promover la discusión de la Ordenanza con la participación de la comunidad.<br><br>
* Presentar la Ordenanza ante el Consejo Municipal, para su discusión y aprobación.<br><br>
* Promover e instrumentar la Ordenanza aprobada.<br><br>
* Creación de la Oficina de Control del Consejo Local de Planificación para el registro de las organizaciones civiles del Municipio.<br><br>
* Exhortación a la comunidades a formalizar su organización ante la Oficina de Registro Subalterno y la Oficina de Control del CLPP.<br><br>
* Promoción la Asamblea de Ciudadanos, a través de los medios de comunicación.<br><br>
* Convocatoria a las Asambleas de Ciudadanos, por parroquia o sector para institucionalización de los Consejos Parroquiales y Comunidades.<br><br>
* Conducción de las Asambleas de Ciudadanos, para la promoción de los CLPP, juramentación de la comunidades, como Consejos Parroquiales y Comunales y la elección de representantes, al CLPP y posteriormente a los Consejos Permanentes.<br><br>
* Acciones para la instalación del CLPP:<br><br>
* Convocar a los integrantes del CLPP a Asamblea de instalación, entregar credenciales, juramentar los integrantes del CLPP y convocar la segunda Asamblea.<br><br>
* Una vez constituida el CLPP, se elaborará el Reglamento interno de Funcionamiento, el cual deberá ser aprobado en Asamblea.<br><br>
