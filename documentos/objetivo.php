<h2>Objetivos</h2>
<p id="pacercade">
La planificación integral del municipio, es la misión fundamental del Consejo, por los cual su gestión debe orientarse hacia la consecución de los objetivos, expresados, en el Artículo 2 de la Ley de los Consejos Locales de Planificación Pública, siendo estos:<br><br>
* Lograr la integración de las comunidades organizadas y grupos vecinales mediante la participación y el protagonismo dentro de una política general de Estado, descentralización y desconcentración de competencias y recursos, de conformidad con lo establecido en la Constitución de la República Bolivariana de Venezuela.<br><br>
* Promover y orientar una tipología de municipio atendiendo, a las condiciones de población, nivel de progreso económico, capacidad para generar ingresos fiscales propios, situación geográfica, elementos históricos, culturales y otros factores relevantes.<br><br>
Es de acotar, que el alcance de los objetivos designados al CLPP, supone la consolidación de las bases principales y política, para la interacción dinámica del crecimiento económico sostenido, efectivas oportunidades y equidades sociales, dinámica territorial ambiental sustentable, ampliación de las oportunidades ciudadanas y diversificación multipolar de las relaciones internacionales, lo cual se fundamenta en la participación protagónica del pueblo, sin discriminación alguna.
La concentración de lo anteriormente expuesto requiere de una serie de estrategias que orientan las acciones concentradas entre los entes gubernamentales y la sociedad civil organizada, como serían las siguientes:<br><br>
* Promover la organización social, mediante el establecimiento de Consejos Parroquiales y Comunales que garanticen la participación protagónica del colectivo, en los procesos de planificación pública local.<br><br>
* Coordinar la participación social, a través de la creación de instancias que faciliten la organización y el funcionamiento del Consejo, a los fines de propósitos contenidos en la Ley del CLPP.<br><br>
* Orientar la elaboración del Plan Municipal de Desarrollo, mediante la participación concertada en programas y proyectos que impulsen el desarrollo local, bajo criterios de sostenibilidad y sustentabilidad.<br><br>
* Propiciar la inversión municipal, mediante el establecimiento de mecanismos de auto - gestión y cogestión, orientados al desarrollo local.<br><br>
* Facultar a la comunidad organizada, a través de estrategias orientadas al dominio de los procesos de planificación, a fin de canalizar las acciones hacia el desarrollo del municipio.<br><br>
</p>
